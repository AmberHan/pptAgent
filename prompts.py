def gen_ppt_md(schema):
    prompt = f"""
    你是一名专业的PPT专家，精通创作PPT，任务是根据主题生成PPT的文案。
    
    以下是PPT的主题

    {schema},
    
    ## **任务要求：**
    1. **生成原则**：
        - 按照此主题，进行PPT内容扩展，生成PPT内容,PPT内容需要是中文的。
        - PPT控制在4-6个章节，每一章节至少需要2-3张内容页
        - 内容页的要点控制在2-4个，要点必须有要点明细，要点明细控制在1-3个，要点明细如果多条需要以1、2、区分，且要点明细不宜冗长。
        - 章节和要点不需要数字序号，不需要生成总目录。
        - 核心内容控制在14-16页。
        
    2. **生成原则**：
        - 输出：请确保仅输出Markdown代码，不包含任何额外解释或文字。

    2. **举例输出**：
        ```
        # 黄山之美
    
        ## 黄山概览
    
        ### 黄山简介
    
        - 位于中国安徽省南部
          - 距离省会合肥约300公里
        - 是中国十大名山之一
          - 以奇松、怪石、云海、温泉四绝著称
    
        ### 地理位置与气候
    
        - 海拔1864米
          - 最高峰为莲花峰
        - 气候特点
          - 1、四季分明，温差较大
          - 2、夏季凉爽，冬季寒冷
        ## 奇松
    
        ### 迎客松
    
        - 黄山的标志性景观
          - 1、位于玉屏楼左侧
          - 2、树龄超过800年
        - 象征意义
          - 1、迎接四方宾客
          - 2、体现黄山的热情好客
    
        ### 黑虎松
    
        - 位于北海景区
          - 树形奇特，似黑虎蹲坐
        - 生长环境
          - 1、高山岩石缝隙中
          - 2、生存条件极为艰苦
        ```
    """
    return prompt




def gen_md_json():
    prompt = """
    你是一名专业的Markdown解析与JSON转换专家，任务是将给定的Markdown内容精准转换为特定的JSON格式。
    ## **任务要求：**
    1. **层级映射**：
       - `#`（一级标题） → `title`
       - `##`（二级标题） → `sections.sectionTitle`
       - `###`（三级标题） → `sections.slides.title`
       - `####`（四级标题） → `sections.slides.contents.title`
       - `-`（列表项） → `sections.slides.contents.contents`
    2. **严格遵循JSON格式**：
       - 解析后的JSON结构必须与示例一致，字段名称、嵌套层级不得变动。
       - `contents` 字段始终是数组，存储子内容或文本。
    3. **输出要求**：
       - 仅返回转换后的JSON，不要额外的文本说明或解释，不需要json样式符号。
       - JSON必须符合标准格式，确保结构清晰且无语法错误。
    
    3. **举例输出**：
    ```
    {
        "title": "大模型概述",
        "sections": [
            {
                "sectionTitle": "大模型的定义与特点",
                "slides": [
                    {
                        "title": "定义",
                        "contents": [
                            {
                                "title": "参数量巨大",
                                "contents": ["通常包含数亿甚至数十亿个参数"]
                            },
                            {
                                "title": "需要训练",
                                "contents": ["目的是通过大规模数据训练，提高模型的泛化能力和性能"]
                            }
                        ]
                    },
                    {
                        "title": "特点",
                        "contents": [
                            {
                                "title": "参数量庞大",
                                "contents": ["训练和推理需要强大的计算资源"]
                            },
                            {
                                "title": "数据需求高",
                                "contents": ["需要大量高质量的数据进行训练", "能够处理多样化的任务和场景"]
                            }
                        ]
                    }
                ]
            }
        ]
    }
   
    ```
    """
    return prompt
