def gen_ppt_md(schema):
    prompt = f"""以下是PPT的主题

    {schema},

    按照此主题，进行PPT内容扩展，生成PPT内容,PPT内容需要是中文的,目录控制在4-6个章节，每一章节需要2-3张内容页，内容页的要点控制在2-4个（必须包含要点和要点明细，要点明细控制在1-3个，要点明细如果多条需要以1、2、区分），要点明细不宜冗长，核心内容控制在14-16页，章节和要点不需要数字序号，不需要包含目录。

    输出：请确保仅输出Markdown代码，不包含任何额外解释或文字。

    例如输出为：
    ```
    # 黄山之美

    ## 黄山概览

    ### 黄山简介

    - 位于中国安徽省南部
      - 距离省会合肥约300公里
    - 是中国十大名山之一
      - 以奇松、怪石、云海、温泉四绝著称

    ### 地理位置与气候

    - 海拔1864米
      - 最高峰为莲花峰
    - 气候特点
      - 1、四季分明，温差较大
      - 2、夏季凉爽，冬季寒冷
    ## 奇松

    ### 迎客松

    - 黄山的标志性景观
      - 1、位于玉屏楼左侧
      - 2、树龄超过800年
    - 象征意义
      - 1、迎接四方宾客
      - 2、体现黄山的热情好客

    ### 黑虎松

    - 位于北海景区
      - 树形奇特，似黑虎蹲坐
    - 生长环境
      - 1、高山岩石缝隙中
      - 2、生存条件极为艰苦
    ```
    """
    return prompt
